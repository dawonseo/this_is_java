package ch06;

public class Printer {
    static void println(int keyword) {
        System.out.println(keyword);
    }

    static void println(boolean keyword) {
        System.out.println(keyword);
    }

    static void println(double keyword) {
        System.out.println(keyword);
    }

    static void println(String keyword) {
        System.out.println(keyword);
    }
}
